// Low-level I2C
static esp_err_t write_cmd(const ssd1306_t *display, const uint8_t cmd) {
    const uint8_t buf[2] = {0x00, cmd}; // 0x00 = Co=0, D/C#=0 (command)
    return i2c_master_write_to_device(display->port, SSD1306_I2C_ADDR,
                                      buf, sizeof(buf),
                                      pdMS_TO_TICKS(10));
}

esp_err_t write_cmd_retry(const ssd1306_t *display, const uint8_t cmd, const int retries) {
    assert(retries >= 1);

    esp_err_t ret = ESP_OK;
    for (int i = 0; i < retries; i++) {
        ret = write_cmd(display, cmd);
        if (ret == ESP_OK) return ESP_OK;
        vTaskDelay(pdMS_TO_TICKS(5)); // маленькая пауза между попытками
    }
    ESP_LOGE(TAG, "Command 0x%02X failed after %d retries: %s", cmd, retries, esp_err_to_name(ret));
    return ret;
}

static esp_err_t write_data(const ssd1306_t *display) {
    void* const cmd = i2c_cmd_link_create();
    i2c_master_start(cmd);
    i2c_master_write_byte(cmd, SSD1306_I2C_ADDR << 1 | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(cmd, 0x40, true);
    i2c_master_write(cmd, display->fb, sizeof(display->fb), true);
    i2c_master_stop(cmd);
    const esp_err_t ret = i2c_master_cmd_begin(display->port, cmd, pdMS_TO_TICKS(50));
    i2c_cmd_link_delete(cmd);
    return ret;
}

// Public API
esp_err_t ssd1306_init(ssd1306_t *display, const i2c_port_t port, const int sda, const int scl, const uint32_t clk_hz) {
    display->port = port;

    i2c_driver_delete(display->port);

    const i2c_config_t cfg = {
        .mode             = I2C_MODE_MASTER,
        .sda_io_num       = sda,
        .scl_io_num       = scl,
        .sda_pullup_en    = GPIO_PULLUP_ENABLE,
        .scl_pullup_en    = GPIO_PULLUP_ENABLE,
        .master.clk_speed = clk_hz,
    };
    ESP_ERROR_CHECK(i2c_param_config(display->port, &cfg));
    ESP_ERROR_CHECK(i2c_driver_install(display->port, I2C_MODE_MASTER, 0, 0, 0));

    vTaskDelay(pdMS_TO_TICKS(50));

    // Initialization sequence SSD1306
    const uint8_t init_seq[] = {
        0xAE,       // Display OFF
        0xD5, 0x80, // Set Display Clock Divide Ratio
        0xA8, 0x3F, // Set Multiplex Ratio (64)
        0xD3, 0x00, // Set Display Offset
        0x40,       // Set Display Start Line = 0
        0x8D, 0x14, // Charge Pump ON
        0x20, 0x00, // Memory Addressing Mode: Horizontal
        0xA1,       // Segment Re-map (зеркало по горизонтали)
        0xC8,       // COM Output Scan Direction (зеркало по вертикали)
        0xDA, 0x12, // COM Pins Hardware Config
        0x81, 0xCF, // Set Contrast
        0xD9, 0xF1, // Pre-charge Period
        0xDB, 0x40, // VCOMH Deselect Level
        0xA4,       // Entire Display ON (показывать RAM)
        0xA6,       // Normal Display (не инвертированный)
        0xAF,       // Display ON
    };

    for (size_t i = 0; i < sizeof(init_seq); i++) {
        const esp_err_t ret = write_cmd_retry(display, init_seq[i], 3);
        if (ret != ESP_OK) {
            ESP_LOGE(TAG, "Init failed at cmd 0x%02X: %s", init_seq[i], esp_err_to_name(ret));
            return ret;
        }
    }

    ssd1306_clear(display);
    ssd1306_refresh(display);
    ESP_LOGI(TAG, "SSD1306 initialized OK");
    return ESP_OK;
}

void ssd1306_clear(ssd1306_t *display) {
    memset(display->fb, 0x00, sizeof(display->fb));
}

void ssd1306_fill(ssd1306_t *display) {
    memset(display->fb, 0xFF, sizeof(display->fb));
}

void ssd1306_draw_pixel(ssd1306_t *display, const int x, const int y, const bool on) {
    if (x < 0 || x >= CONFIG_SSD1306_WIDTH || y < 0 || y >= CONFIG_SSD1306_HEIGHT) return;
    const int byte_idx = x + y / 8 * CONFIG_SSD1306_WIDTH;
    const uint8_t bit  = 1 << (y % 8);
    if (on) display->fb[byte_idx] |=  bit;
    else    display->fb[byte_idx] &= ~bit;
}

void ssd1306_draw_hline(ssd1306_t *display, const int x, const int y, const int len, const bool on) {
    for (int i = 0; i < len; i++) ssd1306_draw_pixel(display, x + i, y, on);
}

void ssd1306_draw_vline(ssd1306_t *display, const int x, const int y, const int len, const bool on) {
    for (int i = 0; i < len; i++) ssd1306_draw_pixel(display, x, y + i, on);
}

void ssd1306_draw_rect(ssd1306_t *display, const int x, const int y, const int w, const int h, const bool on) {
    ssd1306_draw_hline(display, x,         y,         w, on);
    ssd1306_draw_hline(display, x,         y + h - 1, w, on);
    ssd1306_draw_vline(display, x,         y,         h, on);
    ssd1306_draw_vline(display, x + w - 1, y,         h, on);
}

void ssd1306_fill_rect(ssd1306_t *display, const int x, const int y, const int w, const int h, const bool on) {
    for (int row = y; row < y + h; row++)
        ssd1306_draw_hline(display, x, row, w, on);
}

void ssd1306_draw_char(ssd1306_t *display, const int x, const int y, char c, const bool on) {
    if (c < 32 || c > 127) c = '?';
    const uint8_t *glyph = font6x8[c - 32];
    for (int col = 0; col < 6; col++) {
        const uint8_t bits = glyph[col];
        for (int row = 0; row < 8; row++) {
            ssd1306_draw_pixel(display, x + col, y + row, on && bits & 1 << row);
        }
    }
}

void ssd1306_draw_string(ssd1306_t *display, int x, const int y, const char *str, const bool on) {
    while (*str) {
        ssd1306_draw_char(display, x, y, *str++, on);
        x += 6;
        if (x + 6 > CONFIG_SSD1306_WIDTH) break;
    }
}

void ssd1306_refresh(const ssd1306_t *display) {
    // Set a fullscreen range of pages and columns

    // Column 0..127
    write_cmd(display, 0x21);
    write_cmd(display, 0);
    write_cmd(display, 127);
    // Page 0..7
    write_cmd(display, 0x22);
    write_cmd(display, 0);
    write_cmd(display, 7);

    write_data(display);
}
